
-- CUSTOMERS QULITY CHECK
SELECT * FROM BRONZE.CUSTOMERS; 

SELECT
    SUM(CASE WHEN COMPANY_NAME IS NULL OR TRIM(COMPANY_NAME)='' THEN 1 ELSE 0 END) AS NOISE_COMPANY_NAME,
    SUM(CASE WHEN CONTACT_NAME IS NULL OR TRIM(CONTACT_NAME)='' THEN 1 ELSE 0 END) AS NOISE_CONTACT_NAME,
    SUM(CASE WHEN COUNTRY IS NULL OR TRIM(COUNTRY)='' THEN 1 ELSE 0 END) AS NOISE_COUNTRY,
    SUM(CASE WHEN CITY IS NULL OR TRIM(CITY)='' THEN 1 ELSE 0 END) AS NOISE_CITY,
    SUM(CASE WHEN REGION IS NULL OR TRIM(REGION)='' OR TRIM(REGION)='NULL' THEN 1 ELSE 0 END) AS NOISE_REGION
FROM BRONZE.CUSTOMERS;



SELECT CUST_ID,COUNT(*) AS TOTAL_REPEAT
FROM BRONZE.CUSTOMERS
GROUP BY CUST_ID
HAVING COUNT(*) > 1;


