SELECT 
    ORDER_DATE ,
    REQUIRED_DATE,
    SHIPPED_DATE
FROM BRONZE.ORDERS
WHERE LENGTH(TRIM(ORDER_DATE)) != 8 OR LENGTH(TRIM(REQUIRED_DATE)) != 8 ;

SELECT ORDER_ID,COUNT(*) AS TOTAL_REPEAT
FROM BRONZE.ORDERS
